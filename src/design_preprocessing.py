def generate_comments(agents, design_code: str) -> str:
    """
    Generate comments for the design code using the Comment_Generation agent.

    Args:
        agents (dict): A dictionary of agents, including 'Comment_Generation'.
        design_code (str): The original design code to generate comments for.

    Returns:[]
        str: The design code with comments generated by the agent.
    """
    # Prepare the prompt by replacing the placeholder with the actual design code
    design_comment_generation = """
Please analyze the following RTL design code by commenting on each line and block directly within the code. Focus on extracting key features, functionalities, and signal interactions to help generate reliable assumptions and assertions. Provide clear and detailed explanations for how each part of the code contributes to the overall design. The comments should be included within the `verilog` block wrapping.

Input Design Code:
design_code_PH

Answer:
"""
    # Wrap the design code to preserve formatting and indicate the language
    wrapped_design_code = f"```verilog\n{design_code}\n```"

    # Replace the placeholder with the actual design code
    prompt_with_design = design_comment_generation.replace('design_code_PH', wrapped_design_code)

    # Use the Comment_Generation agent to generate the commented design code
    commented_design_response = initiate_chat_with_retry(
        agents["user"],
        agents["Comment_Generation"],
        message=prompt_with_design
    )

    # Parse the response to extract the commented code
    commented_design_code = utils2.parse_code_response_verilog(commented_design_response)

    return commented_design_code

