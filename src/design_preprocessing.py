def preprocess_design(agents, design_code: str) -> str:
    """
    Preprocess the design code by adding comments using the Comment_Generation agent.

    Args:
        agents (dict): A dictionary of agents, including 'Comment_Generation'.
        design_code (str): The original design code to preprocess.

    Returns:
        str: The commented design code.
    """
    if FLAGS.add_comment:
        # Call the generate_comments function to add comments
        commented_design_code = generate_comments(agents, design_code)
        return commented_design_code
    elif FLAGS.use_ICRL:
        # If no preprocessing is needed, return the original design code
        design_meta = generate_line_block_spec(agents, design_code)
        return design_meta
    else:
        return design_code

